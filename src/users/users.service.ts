import { HttpException, HttpStatus, Injectable } from '@nestjs/common';
import { InjectModel } from '@nestjs/sequelize';
import { User } from './user.model';
import { CreateUserDto } from './dto/create-user-dto';
import { RolesService } from '../roles/roles.service';
import { Role } from '../roles/roles.model';
import { AddRoleDto } from './dto/add-role.dto';
import { BanUserDto } from './dto/ban-user.dto';
import { RedisService } from 'src/redis/redis.service';

@Injectable()
export class UsersService {
  constructor(
    @InjectModel(User) private userRepo: typeof User,
    private rolesService: RolesService,
    private redisService: RedisService,
  ) {}

  async createUser(dto: CreateUserDto) {
    console.log('üì• –í—Ö–æ–¥—è—â–∏–µ –¥–∞–Ω–Ω—ã–µ createUser:', JSON.stringify(dto, null, 2));
    // –ë–µ—Ä–µ–º –ø–µ—Ä–≤—É—é —Ä–æ–ª—å –∏–∑ –º–∞—Å—Å–∏–≤–∞ –∏–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–µ–º USER –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
    const roleValue = dto.roles && dto.roles.length > 0 ? dto.roles[0] : 'USER';

    const role = await this.rolesService.getRoleByValue(roleValue);
    if (!role) {
      throw new Error(`–†–æ–ª—å ${roleValue} –Ω–µ –Ω–∞–π–¥–µ–Ω–∞`);
    }

    const candidate = await User.findOne({
      where: { email: dto.email },
    });

    if (candidate) {
      console.log('–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å —Ç–∞–∫–∏–º email —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç:', dto.email);
      throw new Error('–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å —Ç–∞–∫–∏–º email —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç');
    }

    const user = await this.userRepo.create(dto);

    const userPlain = user.get({ plain: true });

    await user.$set('roles', role.id);
    user.roles = [role];

    const userWithRoles = await this.userRepo.findByPk(user.id, {
      include: [
        {
          model: Role,
          as: 'roles',
          through: { attributes: [] },
          attributes: ['id', 'value', 'description'],
        },
      ],
    });

    if (!userWithRoles) {
      throw new Error('–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è');
    }

    // –ü–æ–ª—É—á–∞–µ–º —á–∏—Å—Ç—ã–π –æ–±—ä–µ–∫—Ç —Å —Ä–æ–ª—è–º–∏
    const result = userWithRoles.get({ plain: true });

    console.log('‚úÖ‚úÖ‚úÖ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω:', {
      id: result.id,
      email: result.email,
      roles: result.roles || [],
    });

    return result;
  }

  async getAllUsers() {
    const cachedUsers = await this.redisService.get('users');
    if (cachedUsers) {
      console.log('‚úÖ‚úÖ‚úÖ –ü–æ–ª—É—á–µ–Ω–∏–µ –¥–∞–Ω–Ω—ã—Ö –∏–∑ –∫—ç—à–∞');
      return cachedUsers;
    }

    const users = await this.userRepo.findAll({
      include: { all: true },
    });
    await this.redisService.set('users', users);
    return users;
  }

  async getUserByEmail(email: string) {
    console.log('üîç –ü–æ–∏—Å–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø–æ email:', email);

    const user = await this.userRepo.findOne({
      where: { email },
      include: [
        {
          model: Role,
          as: 'roles',
          through: { attributes: [] }, // –£–±–∏—Ä–∞–µ–º –ª–∏—à–Ω–∏–µ –ø–æ–ª—è –∏–∑ —Å–≤—è–∑–∏
          attributes: ['id', 'value', 'description'],
        },
      ],
    });

    if (!user) {
      console.log('‚ùå –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω');
      return null;
    }

    // –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ –ø—Ä–æ—Å—Ç–æ–π –æ–±—ä–µ–∫—Ç
    const userPlain = user.get({ plain: true });

    console.log('‚úÖ –ù–∞–π–¥–µ–Ω –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å:', {
      id: userPlain.id,
      email: userPlain.email,
      roles: userPlain.roles || [],
    });

    return userPlain;
  }

  async addRole(dto: AddRoleDto) {
    const user = await this.userRepo.findByPk(dto.userId);
    if (!user) {
      throw new Error('–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω');
    }
    const role = await this.rolesService.getRoleByValue(dto.value);
    if (!role) {
      throw new HttpException('–†–æ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω–∞', HttpStatus.NOT_FOUND);
    }
    await user.$add('role', role.id);
    return user;
  }

  async ban(dto: BanUserDto) {
    const user = await this.userRepo.findByPk(dto.userId);
    if (!user) {
      throw new Error('–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω');
    }
    user.banned = true;
    user.banReason = dto.reason;
    await user.save();
    await user.reload();
    console.log('‚úÖ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∑–∞–±–∞–Ω–µ–Ω:', {
      id: user.id,
      email: user.email,
      banned: user.banned,
      banReason: user.banReason,
    });
    return user;
  }
}
